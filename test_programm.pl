displayList([]).
displayList([T|Q]) :- write(' '), write(T), displayList(Q).
displayBoard([]).
displayBoard([T|Q]) :- displayList(T), nl, displayBoard(Q).

displayStaticBoard :- staticBoard(X),
						displayBoard(X), !.


