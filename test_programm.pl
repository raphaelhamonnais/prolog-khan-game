%% ============================ ASK DISPLAY TO PLAYER ============================
%% ===============================================================================

askDisplayToPlayer(X) :- 	write('Please choose you side : a, b, c or d.'),
							nl,
							read(X).

initBoard(BOARD) :- displayStaticBoard(),
					askDisplayToPlayer(VAL),
					chooseBoardDisplay(VAL, BOARD),
					displayBoard(BOARD),!.

